    <h2 style="color:#f8e9d1">Pourquoi l'imprévu ?</h2>
    <p style="color: #ffffff">L'imprévu est ce qui rend chaque voyage unique. C'est l'excitation de ne pas savoir ce qui vous attend, l'aventure de découvrir des lieux inexplorés et de vivre des expériences inoubliables.</p>
    <div class="grille1">
        <div class="box1">
            <h3>Aventure</h3>
            <p>Vivez des aventures uniques et inoubliables, en explorant des destinations surprenantes qui se dévoilent au fil du voyage. Laissez-vous porter par l’excitation de l’inattendu, par ces lieux que vous n’auriez peut‑être jamais imaginé découvrir et qui deviennent pourtant des coups de cœur. Chaque étape devient l’occasion de créer des souvenirs mémorables, de vivre des moments intenses, de rencontrer de nouvelles cultures et de ressentir pleinement la magie de l’évasion. Parce qu’un voyage ne se résume pas à une destination, mais à tout ce qu’il vous fait vivre en chemin.</p>
        </div>
        <div class="grille2">
            <div class="box2">
                <h3>Spontané</h3>
                <p>Profitez de la spontanéité de chaque moment, en laissant l’aventure vous guider vers des expériences inattendues, des rencontres surprenantes et des instants qui ne ressemblent à aucun autre. Laissez-vous porter par ce qui se présente, et transformez chaque découverte en un souvenir authentique qui vous accompagnera longtemps.</p>
            </div>
            <div class="grille3">
                <div class="box3">
                    <h3>Accessible</h3>
                    <p>Nos voyages sont pensés pour être accessibles à tous, avec des options variées et une expérience agréable pour chaque voyageur, le tout à des prix compétitifs.</p>
                </div>
                <div class="box4">
                    <h3>Communauté</h3>
                    <p>Rejoignez une communauté de voyageurs passionnés, en partageant des expériences et en créant des liens durables autour de l'aventure.</p>
